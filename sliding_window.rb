# 尺取り
# 
# 典型問題
# - 正の数列で「区間和 <= S」を満たす最長長さや個数を数える
# - 文字列で「異なる文字しゅ <= K」の最長部分文字列など
# 
# 基本の考え方
# - 区間を伸ばす/縮める 二つのポインタで、単調に動かしながら条件を満たす区間の最適値を探す
# - 非負(または増やすと悪化方向が単調)な量だと、左に戻す必要がなくなるので効く

# 問題
# 正の整数列Aと上限Sが与えられる。区間和がS以下となる最長部分配列の長さを求めよ。
# 
# input:
# 5 20
# 1 5 10 15 20
# 
# output:
# 3

# 方針: 右に一つずつ進めつつ、和がSを超えたら左に縮める
# 計算量: 左右ポインタは各最大N回動くのでO(n)
n, s = gets.split(' ').map(&:to_i)
a = gets.split(' ').map(&:to_i)

ans = 0
sum = 0
l = 0
(0...n).each do |r|
  sum += a[r]
  while sum > s && l <= r
    sum -= a[l]
    l += 1
  end

  ans = [ans, r - l + 1].max
end

puts ans
